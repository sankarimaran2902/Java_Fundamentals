package Fundamentals.Oops_Encapsulation;

public class DressShop
{

    private int dressId;
    private String dressName;
    private String dressCategory;
    private String dressSection;
    private double dressPrice;
    public int getDressId()
    {
        return dressId;
    }
    public void setDressId(int dressId)
    {
        this.dressId=dressId;
    }
    public String getDressName()
    {
        return dressName;
    }
    public void setDressName(String dressName)
    {
        this.dressName=dressName;
    }
    public String getDressCategory()
    {
        return dressCategory;
    }
    public void setDressCategory(String dressCategory)
    {
        this.dressCategory=dressCategory;
    }
    public String getDressSection()
    {
        return dressSection;
    }
    public void setDressSection(String dressSection)
    {
        this.dressSection=dressSection;
    }
    public double getDressPrice()
    {
        return dressPrice;
    }
    public void setDressPrice(double dressPrice)
    {
        this.dressPrice=dressPrice;
    }
}


