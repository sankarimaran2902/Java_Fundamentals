package Fundamentals.String_Methods;

public class Trim_Method
{
    public static void main(String[] args)
    {
        String name="Sivasankari Valli";
        System.out.println(name+"       Siyam");
        System.out.println(name+"       Maran");
        System.out.println(name.trim()+"Siyam");
        System.out.println(name.trim()+"Maran");
    }
}
