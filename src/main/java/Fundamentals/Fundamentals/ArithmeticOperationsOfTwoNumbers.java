package Fundamentals.Fundamentals;

public class ArithmeticOperationsOfTwoNumbers
{
    public static void main(String[] args)
    {
        int firstNumber=125;
        int secondNumber=24;
        int addition=firstNumber+secondNumber;
        System.out.println("Addition of Two Numbers(125+24): "+addition);
        int subtraction=firstNumber-secondNumber;
        System.out.println("Subtraction of Two Numbers(125-24): "+subtraction);
        int multiplication=firstNumber*secondNumber;
        System.out.println("Multiplication of Two Numbers(125*24) :"+multiplication);
        int division=firstNumber/secondNumber;
        System.out.println("Division of Two Numbers(125/24) :"+division);
        int modulo=firstNumber%secondNumber;
        System.out.println("Modulo of Two Numbers(125%24) :"+modulo);
    }
}
