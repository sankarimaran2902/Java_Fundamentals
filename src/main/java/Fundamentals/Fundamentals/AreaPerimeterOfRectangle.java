package Fundamentals.Fundamentals;

public class AreaPerimeterOfRectangle
{
    public static void main(String[] args)
    {
     float width=5.6f;
     float height=8.5f;
     float areaOfRectangle=width*height;
     float perimeterOfRectangle=2*(width+height);
     System.out.println("Area Of Rectangle is 5.6*8.5 ="+areaOfRectangle);
     System.out.println("Perimeter Of Rectangle is 2*(5.6+8.5) ="+perimeterOfRectangle);

    }
}
