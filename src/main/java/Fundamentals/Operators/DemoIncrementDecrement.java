package Fundamentals.Operators;

public class DemoIncrementDecrement
{
    public static void main(String[] args)
    {
        //Pre increment-It increment or decrement the value before execution(concept inside execution)
        int cosmo=200;
        int alpha=199;
        System.out.println(++cosmo);
        System.out.println(--alpha);
        //Post Increment-It increment or decrement the value after execution(concept inside execution)
        System.out.println(cosmo++);
        System.out.println(alpha--);

    }
}

