package Fundamentals.VariablesDatatypes;
public class CostpriceOfItems
{
    public static void main(String[] args)
    {
        int item = 50;
        float costPrice = 9.99f;
        double totalCost = item * costPrice;
        char currency='$';
        System.out.println("Total number of Items are:"  +item);
        System.out.println("Cost Price of the Items are:" +currency +costPrice);
        System.out.println("Total Cost of the Items are:" +currency +totalCost);
    }
}
